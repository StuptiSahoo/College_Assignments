package AD;

public class D6Q1 {
    public class Node {
        // Creating a node of the linkedlist
        public static class node {
            int data;
            Node next;

            // constructor for node with given value and null pointer to the next element in
            // list.
            public node(int data) {
                this.data = data;
                this.next = null;
            }
        }
    }
}
